---Converts a utf8 string to lowercase. Extents string.lower which support only ASCII
---@param str string
---@return string
function utf8.tolower(str)
	local new_str = ""

	if (str and #str ~= 0) then
		for c in str:gmatch("[%z\1-\127\194-\244][\128-\191]*") do
			new_str = new_str .. (utf8.uc_lc[c] or c)
		end
	end

	return new_str
end

return utf8
